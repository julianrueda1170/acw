    public void actualizarListadoAnimales(){

        List<Animal> animales = usuarioLogica.obtenerAnimalesDeUsuario(usuario.getCedula());
        Animal[] animalData = null;
        if(animales != null){

            animalData = new Animal[animales.size()];
            this.listaAnimalesAdoptados.setListData(animales.toArray(animalData));
        }
    }

