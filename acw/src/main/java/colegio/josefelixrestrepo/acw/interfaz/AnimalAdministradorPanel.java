    private void botonEliminarAnimalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarAnimalActionPerformed

        Animal animalSeleccionado = listaAnimales.getSelectedValue();

        if(animalSeleccionado != null){

            int response = JOptionPane.showConfirmDialog(this, "Â¿Deseas eliminar el animal del sistema?");

            if(response == JOptionPane.OK_OPTION){

                int codigoAnimal = animalSeleccionado.getCodigoAnimal();

                String resultado = animalLogica.eliminarAnimal(codigoAnimal);

                if(resultado == "OK"){

                    resultadoLabel.setText("Se ha eliminado el animal correctamente");

                    actualizarListadoAnimales();
                }
                else
                {
                    resultadoLabel.setText(resultado);
                }
            }
        }

    }//GEN-LAST:event_botonEliminarAnimalActionPerformed

