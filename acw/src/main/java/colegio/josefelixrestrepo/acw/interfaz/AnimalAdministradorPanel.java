    private void botonAgregarAnimalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarAnimalActionPerformed

        String tipoAnimal = tipoAnimalCombo.getSelectedItem().toString();
        String raza = razaText.getText();
        String edad = edadText.getText();
        String peso = pesoText.getText();
        String color = colorText.getText();
        String altura = alturaText.getText();

        String resultado = animalLogica.registrarAnimal(tipoAnimal, raza, edad, peso, color, altura);

        if(resultado.equals("OK")){

            actualizarListadoAnimales();
            resultadoLabel.setText("Se ha agregado el animal correctamente.");
        }
        else
        {
            resultadoLabel.setText(resultado);
        }

    }//GEN-LAST:event_botonAgregarAnimalActionPerformed


